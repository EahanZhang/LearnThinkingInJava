public class Practice1 {

    public static void print(String str) {
        System.out.println(str);
    }

    public static void main(String[] args) {
        String str = "Hello World!";

        // 简短的打印语句
        print(str);

        // 正常的打印语句
        System.out.println(str);
    }
}
